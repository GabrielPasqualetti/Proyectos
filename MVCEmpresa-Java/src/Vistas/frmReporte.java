    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        if(!txtImporte.getText().equals("")){
            
            GestorDB gestor = new GestorDB();
            ArrayList<DtoVentaFiltros> lista = gestor.obtenerVentasPorFilroImporteTotal(Double.parseDouble(txtImporte.getText()),txtnombre.getText());

            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"CLIENTE", "PRODUCTO", "IMPORTE"});
            for (DtoVentaFiltros dto : lista) {
                modelo.addRow(new Object[]{dto.getCliente(),dto.getNombre(),dto.getImporte()});
            }
            tblReporteFiltros.setModel(modelo);
            }
        else
            JOptionPane.showMessageDialog(this, "Ingrese un valor para filtrar");
    }//GEN-LAST:event_btnBuscarActionPerformed

