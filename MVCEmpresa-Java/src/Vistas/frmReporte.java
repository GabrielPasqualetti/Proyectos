    private void cargarTabla(){
        GestorDB gestor = new GestorDB();
        ArrayList<DtoReporte> lista = gestor.cantidadVentasPorProducto();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[]{"PRODUCTO","CANTIDAD"});
        for (DtoReporte dto : lista) {
            modelo.addRow(new Object[]{dto.getProducto(),dto.getCantidad()});
        }
        tblReporte.setModel(modelo);
              
    }
    
    private void llenarReporteFacturacion(){
        GestorDB gestor = new GestorDB();
        double facturacion = gestor.facturacionTotal();
        lblFacturacion.setText(String.valueOf(facturacion));
    }
    
    private void llenarReporteVenta(){
        GestorDB gestor = new GestorDB();
        DtoVenta dto = gestor.ventaDeMayorCantidadDeProducto();
        lblCantidad.setText(String.valueOf(dto.getCantidad()));
        lblCliente.setText(dto.getCliente());
        lblProducto.setText(dto.getProducto());
        lblPrecio.setText(String.valueOf(dto.getPrecio()));
    }
